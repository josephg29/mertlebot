const PIN_MAPS = {
  'arduino-uno': UNO_PINS,
  'arduino-mega': MEGA_PINS,
  'arduino-nano': NANO_PINS,
  'esp32-devkit-v1': ESP32_PINS,
};

export function resolvePin(boardX, boardY, boardType, pinName) {
  const pin = PIN_MAPS[boardType]?.[pinName];
  if (!pin) throw new Error(`Unknown pin "${pinName}" on board "${boardType}"`);
  return { x: boardX + pin.x, y: boardY + pin.y };
}
